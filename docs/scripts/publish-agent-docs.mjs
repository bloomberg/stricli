import { copyFile, cp, mkdir, writeFile } from "node:fs/promises";
import { resolve } from "node:path";
import { fileURLToPath } from "node:url";

const docsRoot = fileURLToPath(new URL("..", import.meta.url));
const buildRoot = resolve(docsRoot, "build");
const sourceyRoot = resolve(buildRoot, "agent-docs");
const markdownRoot = resolve(buildRoot, "markdown");

for (const filename of ["llms.txt", "llms-full.txt"]) {
    await copyFile(resolve(sourceyRoot, filename), resolve(buildRoot, filename));
}

await mkdir(resolve(markdownRoot, "packages"), { recursive: true });

for (const packageName of ["core", "auto-complete"]) {
    await cp(resolve(docsRoot, "packages", packageName), resolve(markdownRoot, "packages", packageName), {
        recursive: true,
    });
}

await writeFile(
    resolve(markdownRoot, "index.md"),
    `# Stricli Markdown documentation

These files are generated by TypeDoc from the Stricli TypeScript source during
the documentation build. They are published alongside the HTML documentation so
agents and other tools can read the API reference without parsing HTML.

- [@stricli/core](./packages/core/index.md)
- [@stricli/auto-complete](./packages/auto-complete/index.md)
- [llms.txt](../llms.txt)
- [llms-full.txt](../llms-full.txt)
`,
    "utf8",
);
